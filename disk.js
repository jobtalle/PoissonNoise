/**
 * A disk
 */
export class Disk {
    /**
     * Construct a disk
     * @param {number} x The X position
     * @param {number} y The Y position
     * @param {number} radius The radius
     */
    constructor(x, y, radius) {
        this.x = x;
        this.y = y;
        this.radius = radius;
    }
}